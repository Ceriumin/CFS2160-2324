package Task3;

public class Volunteer {
    private String name;
    private int boxes;

    //Constructor for the Volunteer class
    public Volunteer(String name, int boxes) {
        this.name = name;
        this.boxes = boxes;
    }

    public String getName() {
        return name;
    }

    public int getBoxes() {
        return boxes;
    }
    
}


